//Remove elements at a specific index in an array (splice).

const fruits=[ 'Apple', 'Papaya', 'Guava', 'Banana', 'Orange' ];
fruits.splice(1,3);//1 is index no. where to start and 3 indicates 
                    //how many element should be remove
                                    
console.log(fruits);//output: [ 'Apple', 'Orange' ]